package uk.co.bumblebyte;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.Test;

class DayFourTest {
    @Test
    void exampleOne() {
        char[][] input = {
                {'.', '.', '@', '@', '.', '@', '@', '@', '@', '.',},
                {'@', '@', '@', '.', '@', '.', '@', '.', '@', '@',},
                {'@', '@', '@', '@', '@', '.', '@', '.', '@', '@',},
                {'@', '.', '@', '@', '@', '@', '.', '.', '@', '.',},
                {'@', '@', '.', '@', '@', '@', '@', '.', '@', '@',},
                {'.', '@', '@', '@', '@', '@', '@', '@', '.', '@',},
                {'.', '@', '.', '@', '.', '@', '.', '@', '@', '@',},
                {'@', '.', '@', '@', '@', '.', '@', '@', '@', '@',},
                {'.', '@', '@', '@', '@', '@', '@', '@', '@', '.',},
                {'@', '.', '@', '.', '@', '@', '@', '.', '@', '.',}
        };

        Assertions.assertEquals(13, DayFour.findAccessibleRolls(input, false));
    }

    @Test
    void exampleTwo() {
        char[][] input = {
                {'.', '.', '@', '@', '.', '@', '@', '@', '@', '.',},
                {'@', '@', '@', '.', '@', '.', '@', '.', '@', '@',},
                {'@', '@', '@', '@', '@', '.', '@', '.', '@', '@',},
                {'@', '.', '@', '@', '@', '@', '.', '.', '@', '.',},
                {'@', '@', '.', '@', '@', '@', '@', '.', '@', '@',},
                {'.', '@', '@', '@', '@', '@', '@', '@', '.', '@',},
                {'.', '@', '.', '@', '.', '@', '.', '@', '@', '@',},
                {'@', '.', '@', '@', '@', '.', '@', '@', '@', '@',},
                {'.', '@', '@', '@', '@', '@', '@', '@', '@', '.',},
                {'@', '.', '@', '.', '@', '@', '@', '.', '@', '.',}
        };

        Assertions.assertEquals(43, DayFour.findAccessibleRolls(input, true));
    }

    @Test
    void testCountNoneAdjacent() {
        char[][] input = {
                {'.', '.', '.'},
                {'.', '@', '.'},
                {'.', '.', '.'},
        };

        Assertions.assertEquals(0, DayFour.countAdjacent(input, 1, 1));
    }

    @Test
    void testCountSomeAdjacent() {
        char[][] input = {
                {'@', '.', '@'},
                {'.', '@', '.'},
                {'@', '.', '@'},
        };

        Assertions.assertEquals(4, DayFour.countAdjacent(input, 1, 1));
    }
}